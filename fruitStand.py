"""Temperature Conversion:
A weather report gives the temperature in Celsius as 25 degrees. 
Write a program to convert this temperature to Fahrenheit using the formula: Fahrenheit = (Celsius * 9/5) + 32. 
Store the Celsius temperature in a variable."""

def celsius_to_fahrenheit(celsius):
    fahrenheit = fahrenheit = (celsius * 9/5) + 32
    return fahrenheit




temp = celsius_to_fahrenheit(100)
print(temp)